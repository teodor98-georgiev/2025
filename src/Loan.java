public class Loan {
    String bookId;
    String memberId;
    String loanDate;
    String returnDate;

    public Loan(String bookId, String memberId, String loanDate, String returnDate) {
        this.bookId = bookId;
        this.memberId = memberId;
        this.loanDate = loanDate;
        this.returnDate = returnDate;
    }

}
